    private void vendCodFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_vendCodFocusLost
        VendedorController controll = new VendedorController();
        String[] dados = controll.recover(Integer.parseInt(vendCod.getText()));

        if (dados[0].equals("0")) {
            JOptionPane.showMessageDialog(null, "NÃ£o foi encontrado nenhum registro");
        } else {
            vendName.setText(dados[1]);
            vendEmp.setText(dados[2]);
            vendCpf.setText(dados[3]);

            vendName.setEnabled(true);
            vendEmp.setEnabled(true);
            vendCpf.setEnabled(true);

            vendRemover.setEnabled(true);

//            codInput.requestFocus();
        }
    }//GEN-LAST:event_vendCodFocusLost

    private void vendNovoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_vendNovoMouseClicked
        vendNovo.setEnabled(true);
        vendSalvar.setEnabled(true);
        vendCancelar.setEnabled(true);
        vendRemover.setEnabled(false);

        vendName.setEnabled(true);
        vendEmp.setEnabled(true);
        vendCpf.setEnabled(true);

        limparTela();
    }//GEN-LAST:event_vendNovoMouseClicked

    private void vendSalvarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_vendSalvarMouseClicked
        vendSalvar.requestFocus();
        String dados[] = new String[4];

        dados[0] = vendCod.getText();
        dados[1] = vendName.getText();
        dados[2] = vendEmp.getText();
        dados[3] = vendCpf.getText();

        VendedorController controll = new VendedorController();
        if (controll.save(dados)) {
            JOptionPane.showMessageDialog(null, "Registro inserido com sucesso");
        } else {
            JOptionPane.showMessageDialog(null, "Registro atualizado com sucesso");
        }
    }//GEN-LAST:event_vendSalvarMouseClicked

    private void vendRemoverMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_vendRemoverMouseClicked
        vendRemover.requestFocus();
        VendedorController controll = new VendedorController();
        Integer cod = Integer.parseInt(vendCod.getText());

        if (controll.delete(cod)) {
            limparTela();
            JOptionPane.showMessageDialog(null, "Registro removido com sucesso");
        } else {
            JOptionPane.showMessageDialog(null, "Erro ao remover o registro");
        }
    }//GEN-LAST:event_vendRemoverMouseClicked

    private void vendCancelarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_vendCancelarMouseClicked
        limparTela();

    }//GEN-LAST:event_vendCancelarMouseClicked

